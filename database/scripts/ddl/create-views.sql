CREATE VIEW IF NOT EXISTS VW_QUARTO_DISPONIVEL AS
SELECT	ID_QUARTO,
	NU_QUARTO,
        IN_DISPONIVEL,
	QUARTO.ID_TIPO_QUARTO,
        NO_TIPO_QUARTO,
        VL_DIARIA
FROM QUARTO
LEFT JOIN TIPO_QUARTO ON QUARTO.ID_TIPO_QUARTO = TIPO_QUARTO.ID_TIPO_QUARTO
WHERE QUARTO.IN_DISPONIVEL = 1;

CREATE VIEW IF NOT EXISTS VW_QUARTO_INDISPONIVEL AS
SELECT	ID_QUARTO,
	NU_QUARTO,
        IN_DISPONIVEL,
	QUARTO.ID_TIPO_QUARTO,
        NO_TIPO_QUARTO,
        VL_DIARIA
FROM QUARTO
LEFT JOIN TIPO_QUARTO ON QUARTO.ID_TIPO_QUARTO = TIPO_QUARTO.ID_TIPO_QUARTO
WHERE QUARTO.IN_DISPONIVEL = 0;

CREATE VIEW IF NOT EXISTS VW_QUARTO_TIPO_QUARTO AS
SELECT  ID_QUARTO,
	NU_QUARTO,
        IN_DISPONIVEL,
	QUARTO.ID_TIPO_QUARTO,
        NO_TIPO_QUARTO,
        VL_DIARIA
FROM QUARTO
LEFT JOIN TIPO_QUARTO ON QUARTO.ID_TIPO_QUARTO = TIPO_QUARTO.ID_TIPO_QUARTO;

CREATE VIEW IF NOT EXISTS VW_FUNCIONARIO_ENDERECO AS
SELECT 	ID_FUNCIONARIO,
		NU_CPF,
		NO_FUNCIONARIO,
		NU_TELEFONE,
		NO_EMAIL,
		FUNCIONARIO.ID_ENDERECO,
		NU_CEP,
		NO_ENDERECO,
		NO_COMPLEMENTO,
		NU_NUMERO
FROM FUNCIONARIO
LEFT JOIN ENDERECO ON FUNCIONARIO.ID_ENDERECO = ENDERECO.ID_ENDERECO;

CREATE VIEW IF NOT EXISTS VW_HOSPEDE_ENDERECO AS
SELECT 	ID_HOSPEDE,
		NU_CPF,
		NO_HOSPEDE,
		NU_TELEFONE,
		NO_EMAIL,
		HOSPEDE.ID_ENDERECO,
		NU_CEP,
		NO_ENDERECO,
		NO_COMPLEMENTO,
		NU_NUMERO
FROM HOSPEDE
LEFT JOIN ENDERECO ON HOSPEDE.ID_ENDERECO = ENDERECO.ID_ENDERECO;